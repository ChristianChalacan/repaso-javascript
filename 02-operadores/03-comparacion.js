let a = 10;

//operadores relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//Operadores de igualdad
console.log(a == 10); //consulta el valor pero no el tipo de dato 
console.log(a != 10);
console.log(a === '10');// consulta el valor y el tipo para una comparacion exacta